import os
import logging
from anthropic import AsyncAnthropic, APIError, APIConnectionError, RateLimitError

logger = logging.getLogger(__name__)

# ── Ключ API ────────────────────────────────────────────────
# Лучшая практика — хранить в переменной окружения, а не в коде.
ANTHROPIC_API_KEY = os.environ.get("ANTHROPIC_API_KEY", "")

# Модель: Haiku 4.5 — быстрый и недорогой, отлично подходит для чата
MODEL_NAME = "claude-haiku-4-5"

# Лимиты
MAX_HISTORY_MESSAGES = 20      # храним последние 20 сообщений (10 пар Q/A)
MAX_USER_MESSAGE_LEN = 2000    # ограничение длины пользовательского сообщения
MAX_OUTPUT_TOKENS    = 1024    # ограничение длины ответа

# Клиент Anthropic (создаётся один раз при импорте)
_client: AsyncAnthropic | None = None


def _get_client() -> AsyncAnthropic | None:
    """Лениво создаёт клиента. Возвращает None если ключ не задан."""
    global _client
    if _client is None:
        if not ANTHROPIC_API_KEY:
            logger.error("ANTHROPIC_API_KEY не задан в переменных окружения!")
            return None
        _client = AsyncAnthropic(api_key=ANTHROPIC_API_KEY)
    return _client


