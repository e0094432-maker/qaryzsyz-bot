# ── Главная функция ──────────────────────────────────────────

async def ask_ai_lawyer(user_message: str, history: list, lang: str = "ru") -> tuple[str, list]:
    """
    Отправляет сообщение AI-юристу (Gemini) и возвращает ответ + обновлённую историю.

    Args:
        user_message: текст от пользователя
        history: список предыдущих сообщений в формате
                 [{"role": "user", "content": "..."}, {"role": "assistant", "content": "..."}]
        lang: "ru" или "kz"

    Returns:
        (answer_text, new_history) — ответ ассистента и обновлённая история диалога
    """
    if not _configure():
        msg = (
            "⚠️ AI-юрист временно недоступен (API-ключ не настроен). "
            "Свяжитесь с горячей линией: 8-700-855-85-85"
            if lang == "ru" else
            "⚠️ AI-заңгер уақытша қолжетімсіз (API кілті бапталмаған). "
            "Ыстық желіге хабарласыңыз: 8-700-855-85-85"
        )
        return msg, history

    # Защита от слишком длинных сообщений
    if len(user_message) > MAX_USER_MESSAGE_LEN:
        msg = (
            f"⚠️ Сообщение слишком длинное (макс. {MAX_USER_MESSAGE_LEN} символов). "
            "Разбейте на несколько вопросов покороче."
            if lang == "ru" else
            f"⚠️ Хабарлама тым ұзын (макс. {MAX_USER_MESSAGE_LEN} таңба). "
            "Қысқа сұрақтарға бөліңіз."
        )
        return msg, history

    system_prompt = SYSTEM_PROMPT_RU if lang == "ru" else SYSTEM_PROMPT_KZ

    # Конвертируем историю из формата OpenAI в формат Gemini
    # OpenAI: [{"role": "user/assistant", "content": "..."}]
    # Gemini: [{"role": "user/model", "parts": ["..."]}]
    gemini_history = []
    for msg in history:
        role = "user" if msg["role"] == "user" else "model"
        gemini_history.append({
            "role": role,
            "parts": [msg["content"]]
        })

    # Обрезаем историю если она слишком длинная
    if len(gemini_history) > MAX_HISTORY_MESSAGES:
        gemini_history = gemini_history[-MAX_HISTORY_MESSAGES:]

    try:
        # Создаём модель с системной инструкцией
        model = genai.GenerativeModel(
            model_name=MODEL_NAME,
            system_instruction=system_prompt,
            generation_config={
                "max_output_tokens": MAX_OUTPUT_TOKENS,
                "temperature": 0.7,
            },
        )

        # Запускаем чат с историей
        chat = model.start_chat(history=gemini_history)

        # Отправляем сообщение синхронно через executor (Gemini SDK не async)
        loop = asyncio.get_event_loop()
        response = await loop.run_in_executor(
            None,
            lambda: chat.send_message(user_message)
        )

        # Извлекаем текст ответа
        answer = (response.text or "").strip()

        if not answer:
            answer = (
                "⚠️ Не удалось получить ответ. Попробуйте переформулировать вопрос."
                if lang == "ru" else
                "⚠️ Жауап алу мүмкін болмады. Сұрақты қайта тұжырымдаңыз."
            )
            return answer, history

        # Обновляем историю в формате OpenAI (для совместимости с handlers.py)
        new_history = list(history) + [
            {"role": "user", "content": user_message},
            {"role": "assistant", "content": answer},
        ]

        # Обрезаем если переросло
        if len(new_history) > MAX_HISTORY_MESSAGES:
            new_history = new_history[-MAX_HISTORY_MESSAGES:]

        return answer, new_history

    except Exception as e:
        logger.error(f"Gemini error: {e}", exc_info=True)
        err_str = str(e).lower()

        # Определяем тип ошибки и даём понятное сообщение
        if "quota" in err_str or "rate" in err_str or "429" in err_str:
            msg = (
                "⚠️ Слишком много запросов к AI. Попробуйте через минуту."
                if lang == "ru" else
                "⚠️ AI-ге сұраныс тым көп. Бір минуттан кейін қайталаңыз."
            )
        elif "api key" in err_str or "401" in err_str or "403" in err_str or "permission" in err_str:
            msg = (
                "⚠️ Проблема с настройкой AI. Свяжитесь с поддержкой: 8-700-855-85-85"
                if lang == "ru" else
                "⚠️ AI баптау мәселесі. Қолдау қызметіне хабарласыңыз: 8-700-855-85-85"
            )
        elif "safety" in err_str or "blocked" in err_str:
            msg = (
                "⚠️ Ваш вопрос был отклонён системой безопасности. Попробуйте переформулировать."
                if lang == "ru" else
                "⚠️ Сұрағыңыз қауіпсіздік жүйесімен қабылданбады. Қайта тұжырымдаңыз."
            )
        else:
            msg = (
                "⚠️ Ошибка AI-сервиса. Попробуйте позже или позвоните: 8-700-855-85-85"
                if lang == "ru" else
                "⚠️ AI-қызметінің қатесі. Кейінірек көріңіз немесе қоңырау шалыңыз: 8-700-855-85-85"
            )

        return msg, history
