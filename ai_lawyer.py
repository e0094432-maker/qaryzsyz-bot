# ── Главная функция ──────────────────────────────────────────

async def ask_ai_lawyer(user_message: str, history: list, lang: str = "ru") -> tuple[str, list]:
    """
    Отправляет сообщение AI-юристу и возвращает ответ + обновлённую историю.

    Args:
        user_message: текст от пользователя
        history: список предыдущих сообщений в формате
                 [{"role": "user", "content": "..."}, {"role": "assistant", "content": "..."}]
        lang: "ru" или "kz"

    Returns:
        (answer_text, new_history) — ответ ассистента и обновлённая история диалога

    На любой ошибке возвращает сообщение об ошибке и НЕ изменяет историю.
    """
    client = _get_client()
    if client is None:
        msg = (
            "⚠️ AI-юрист временно недоступен (API-ключ не настроен). "
            "Свяжитесь с горячей линией: 8-700-855-85-85"
            if lang == "ru" else
            "⚠️ AI-заңгер уақытша қолжетімсіз (API кілті бапталмаған). "
            "Ыстық желіге хабарласыңыз: 8-700-855-85-85"
        )
        return msg, history

    # Защита от слишком длинных сообщений
    if len(user_message) > MAX_USER_MESSAGE_LEN:
        msg = (
            f"⚠️ Сообщение слишком длинное (макс. {MAX_USER_MESSAGE_LEN} символов). "
            "Разбейте на несколько вопросов покороче."
            if lang == "ru" else
            f"⚠️ Хабарлама тым ұзын (макс. {MAX_USER_MESSAGE_LEN} таңба). "
            "Қысқа сұрақтарға бөліңіз."
        )
        return msg, history

    # Собираем сообщения: история + новое сообщение пользователя
    messages = list(history) + [{"role": "user", "content": user_message}]

    # Обрезаем историю если она слишком длинная
    if len(messages) > MAX_HISTORY_MESSAGES:
        messages = messages[-MAX_HISTORY_MESSAGES:]

    system_prompt = SYSTEM_PROMPT_RU if lang == "ru" else SYSTEM_PROMPT_KZ

    try:
        response = await client.messages.create(
            model=MODEL_NAME,
            max_tokens=MAX_OUTPUT_TOKENS,
            system=system_prompt,
            messages=messages,
        )

        # Извлекаем текст ответа
        answer_parts = []
        for block in response.content:
            if hasattr(block, "text"):
                answer_parts.append(block.text)
        answer = "\n".join(answer_parts).strip()

        if not answer:
            answer = (
                "⚠️ Не удалось получить ответ. Попробуйте переформулировать вопрос."
                if lang == "ru" else
                "⚠️ Жауап алу мүмкін болмады. Сұрақты қайта тұжырымдаңыз."
            )
            return answer, history

        # Обновляем историю: добавляем вопрос пользователя и ответ ассистента
        new_history = messages + [{"role": "assistant", "content": answer}]

        # Снова обрезаем если переросло
        if len(new_history) > MAX_HISTORY_MESSAGES:
            new_history = new_history[-MAX_HISTORY_MESSAGES:]

        return answer, new_history

    except RateLimitError as e:
        logger.error(f"AI rate limit: {e}")
        msg = (
            "⚠️ Слишком много запросов к AI. Попробуйте через минуту."
            if lang == "ru" else
            "⚠️ AI-ге сұраныс тым көп. Бір минуттан кейін қайталаңыз."
        )
        return msg, history

    except APIConnectionError as e:
        logger.error(f"AI connection error: {e}")
        msg = (
            "⚠️ Проблема с подключением к AI. Проверьте интернет и попробуйте снова."
            if lang == "ru" else
            "⚠️ AI-ге қосылу мәселесі. Интернетті тексеріп, қайта көріңіз."
        )
        return msg, history

    except APIError as e:
        logger.error(f"AI API error: {e}")
        msg = (
            "⚠️ Ошибка AI-сервиса. Попробуйте позже или позвоните на горячую линию: 8-700-855-85-85"
            if lang == "ru" else
            "⚠️ AI-қызметінің қатесі. Кейінірек көріңіз немесе ыстық желіге қоңырау шалыңыз: 8-700-855-85-85"
        )
        return msg, history

    except Exception as e:
        logger.error(f"AI unexpected error: {e}", exc_info=True)
        msg = (
            "⚠️ Неожиданная ошибка. Попробуйте ещё раз или вернитесь в главное меню."
            if lang == "ru" else
            "⚠️ Күтпеген қате. Қайталап көріңіз немесе басты мәзірге оралыңыз."
        )
        return msg, history
