import google.generativeai as genai

logger = logging.getLogger(__name__)

# ── Ключ API из переменной окружения ─────────────────────────
GEMINI_API_KEY = os.environ.get("GEMINI_API_KEY", "")

# Модель — Gemini 1.5 Flash (бесплатная, быстрая, поддерживает русский/казахский)
MODEL_NAME = "gemini-pro"

# Лимиты
MAX_HISTORY_MESSAGES = 20      # храним последние 20 сообщений (10 пар Q/A)
MAX_USER_MESSAGE_LEN = 2000    # ограничение длины пользовательского сообщения
MAX_OUTPUT_TOKENS    = 1024    # ограничение длины ответа

# Настраиваем Gemini один раз при старте
_configured = False


def _configure():
    """Лениво настраивает Gemini. Возвращает False если ключ не задан."""
    global _configured
    if _configured:
        return True
    if not GEMINI_API_KEY:
        logger.error("GEMINI_API_KEY не задан в переменных окружения!")
        return False
    try:
        genai.configure(api_key=GEMINI_API_KEY)
        _configured = True
        return True
    except Exception as e:
        logger.error(f"Не удалось настроить Gemini: {e}")
        return False


