    doc.add_paragraph("Перечень прилагаемых документов:")
    doc.add_paragraph("— Удостоверение личности")
    doc.add_paragraph("— ГКБ отчёт")
    doc.add_paragraph("— Документы, подтверждающие финансовое положение (при наличии)")
    doc.add_paragraph()

    _sign(doc, name)
    return _save(doc)


