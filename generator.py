# ─── Вспомогательные функции ──────────────────────────────────

def _bank_field(bank: dict, field: str, default: str = "___") -> str:
    """Безопасно получает поле из bank, возвращает default если нет."""
    v = bank.get(field, "")
    return str(v).strip() if v else default


def _doc():
    doc = Document()
    s = doc.styles["Normal"]
    s.font.name = "Times New Roman"
    s.font.size = Pt(12)
    for sec in doc.sections:
        sec.top_margin    = Cm(2)
        sec.bottom_margin = Cm(2)
        sec.left_margin   = Cm(3)
        sec.right_margin  = Cm(1.5)
    return doc


def _save(doc):
    buf = io.BytesIO()
    doc.save(buf)
    buf.seek(0)
    return buf


def _right(doc, text, bold=False):
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.RIGHT
    run = p.add_run(text)
    run.bold = bold
    return p


def _center(doc, text, bold=True):
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = p.add_run(text)
    run.bold = bold
    return p


def _indent(doc, text, bold_prefix=None):
    p = doc.add_paragraph()
    p.paragraph_format.first_line_indent = Cm(1.25)
    if bold_prefix:
        r = p.add_run(bold_prefix)
        r.bold = True
    p.add_run(text)
    return p


def _bold(doc, text):
    p = doc.add_paragraph()
    p.paragraph_format.first_line_indent = Cm(1.25)
    r = p.add_run(text)
    r.bold = True
    return p


def _sign(doc, name):
    doc.add_paragraph()
    doc.add_paragraph("«___» ____________ _____ г.")
    doc.add_paragraph()
    p = doc.add_paragraph()
    p.add_run(f"С уважением, {name}  _________________________ (подпись)")


def _fmt(v) -> str:
    try:
        return f"{int(str(v).replace(' ', '').replace(',', '')):,}"
    except Exception:
        return str(v) if v else "___"


def _v(d, *keys, default="___"):
    for k in keys:
        v = d.get(k)
        if v:
            return str(v).strip()
    return default


