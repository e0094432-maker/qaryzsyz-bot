def find_bank(name: str):
    name_l = name.lower().strip()
    for b in BANKS.values():
        for alias in b["names"]:
            if alias in name_l or name_l in alias:
                return b
    return None


def find_bank_in_text(text: str):
    """Find bank by searching for any alias in text. Returns list of (bank, position)."""
    text_l = text.lower()
    results = []
    for b in BANKS.values():
        for alias in b["names"]:
            idx = text_l.find(alias.lower())
            if idx != -1:
                results.append((b, idx))
                break
    results.sort(key=lambda x: x[1])
    return results


